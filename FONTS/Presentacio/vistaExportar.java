    /**
     * Seleciona el path on vols fer el export
     * @param evt
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setCurrentDirectory(new File(System.getProperty("user.dir") + "/data/dades"));
        int returnValue = chooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            path.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    /**
     * Exportar el document amb parametre introduit
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String titul =  jTexttiul.getText();
        String autor =  jTextautor.getText();
        String nmopath = path.getText();
        String nomDocExportat = nom.getText();
        if(titul.length() == 0){
            JOptionPane.showMessageDialog(null,"Titul buit");
        }
        if(autor.length() == 0){
            JOptionPane.showMessageDialog(null,"Autor buit");
        }
        if(nmopath.length() == 0){
            JOptionPane.showMessageDialog(null,"Path buit");
        }
        if(nomDocExportat.length() == 0){
            JOptionPane.showMessageDialog(null,"Nom de document exportat Ã©s buit");
        }
        if(titul.length() != 0 && autor.length() != 0 && nmopath.length() != 0){
            if(CtrlPresentacio.ExisteixDoc(titul, autor)){
                if (format.getSelectedIndex() == 0) {
                    CtrlPresentacio.exportarTXT(titul,autor,nomDocExportat,nmopath);
                }
                else {
                    CtrlPresentacio.exportarXML(titul,autor,nomDocExportat,nmopath);

                }
                JOptionPane.showMessageDialog(null,"Document Exportat");
                jTexttiul.setText(null);
                jTextautor.setText(null);
            }
            else JOptionPane.showMessageDialog(null,"Document no existeix");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

