    /**
     * Seleciona el path on vols fer el export
     * @param evt
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setCurrentDirectory(new File(System.getProperty("user.dir") + "/data/dades"));
        int returnValue = chooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            path.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    /**
     * Exportar el document amb parametre introduit
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nmopath = path.getText();
        String nomDocExportat = nom.getText();
        if(nmopath.length() == 0){
            JOptionPane.showMessageDialog(null,"Path buit");
        }
        if(nomDocExportat.length() == 0){
            JOptionPane.showMessageDialog(null,"Nom de document exportat Ã©s buit");
        }
        if(titol.length() != 0 && autor.length() != 0 && nmopath.length() != 0){
            if(CtrlPresentacio.ExisteixDoc(titol, autor)){
                if (format.getSelectedIndex() == 0) {
                    CtrlPresentacio.exportarTXT(titol,autor,nomDocExportat,nmopath);
                }
                else {
                    CtrlPresentacio.exportarXML(titol,autor,nomDocExportat,nmopath);

                }
                JOptionPane.showMessageDialog(null,"Document Exportat");
              
            }
            else JOptionPane.showMessageDialog(null,"Document no existeix");
        }
        if (ret == 0) {
            vistaConsultaTitolAutor vistaCTA = new vistaConsultaTitolAutor();
            vistaCTA.setVisible(true);
            this.dispose();
        }
        else if (ret == 1) {
            vistaConsultaContingut vistaCC = new vistaConsultaContingut();
            vistaCC.setVisible(true);
            this.dispose();
        }
        else {
            vistaConsultaDocSemblants vistaCDS = new vistaConsultaDocSemblants();
            vistaCDS.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

