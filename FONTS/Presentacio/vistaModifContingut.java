    /**
     * Modifica el contingut d'un document amb els parametres introduits
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           String titul =  jTexttiul.getText();
           String autor =  jTextautor.getText();
           String newcon = jTextNew.getText();
           if(titul.length() == 0){
            JOptionPane.showMessageDialog(null,"Titol buit");
           }
           if(autor.length() == 0){
            JOptionPane.showMessageDialog(null,"Autor buit");
           }
           if(titul.length() != 0 && autor.length() != 0){
                 if(CtrlPresentacio.ExisteixDoc(titul, autor)){
                    CtrlPresentacio.modifContingut(titul,autor,newcon);
                    JOptionPane.showMessageDialog(null,"Document canviat");
                 }
                 else JOptionPane.showMessageDialog(null,"Document no existeix");
          }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
           String titul =  jTexttiul.getText();
           String autor =  jTextautor.getText();
           if(titul.length() == 0){
            JOptionPane.showMessageDialog(null,"Titol buit");
           }
           if(autor.length() == 0){
            JOptionPane.showMessageDialog(null,"Autor buit");
            
           }
           else {
            String contingut = CtrlPresentacio.getContingut(titul, autor);
            jTextNew.setText(contingut);
           }
    }//GEN-LAST:event_jButton3ActionPerformed

